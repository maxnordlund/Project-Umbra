	/**
	 * Translates the point of the event 90 degrees clockwise.
	 * 
	 * @param event
	 */
	private void translate(MouseEvent event){
		int x = event.getX();
		int y = event.getY();
		event.translatePoint(source.getHeight() - y - x, x - y);
	}
	
